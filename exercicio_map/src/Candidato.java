public class Candidato {

    String nome;

    Integer[] voto;

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public Integer[] getVoto() {
        return voto;
    }

    public void setVoto(Integer[] voto) {
        this.voto = voto;
    }
}
